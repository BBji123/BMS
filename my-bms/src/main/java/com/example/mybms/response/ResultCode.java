package com.example.mybms.response;

/**
 * 自定义的响应码
 */
public enum ResultCode {
    SUCCESS(100,"success"),
    FAIL(200,"fail to get data");

    private int code;
    private String msg;

    ResultCode(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
